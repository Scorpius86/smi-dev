use smi_dev;

CREATE TABLE produccion (
	idProduccion INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    idCultivo INT(6) NOT NULL,
    anio INT NOT NULL,
    produccion DECIMAL(18,3) NOT NULL,
    
    FOREIGN KEY fk_cultivo(idCultivo)
	REFERENCES cultivo(id)
	ON UPDATE CASCADE
	ON DELETE RESTRICT
)ENGINE=InnoDB;


